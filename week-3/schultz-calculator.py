#This is the add function. Simply taking two integers and adding them.
#Calling the add function at the end will add the two parameters
def add(num1, num2):
  num3 = num1 + num2
  print(num1, "+" ,num2, "is" ,num3)
add(4,4)

#This is the subtract function. Simply taking two integers and subtracting them.
#Calling the subtracting function at the end will subtract the two parameters
def subtract(num1, num2):
  num3 = num1 - num2
  print(num1, "-" ,num2, "is" ,num3)
subtract(10,6)

#This is the divide function. Simply taking two integers and dividing them.
#Calling the divide function at the end will divide the two parameters
def divide(num1, num2):
  num3 = num1 / num2
  print(num1, "/" ,num2, "is" ,num3)
divide(8,2)

#This is the multiply function. Simply taking two integers and multiplying them.
#Calling the multiply function at the end will multiply the two parameters
def multiply(num1, num2):
  num3 = num1 * num2
  print(num1, "*" ,num2, "is" ,num3)
multiply(10,2)

